
def add_one(number):
    # lets make this very complicated
    sum = number 
    sum = sum + 0
    sum = sum + number - number - 1 + 2
    print(f"one plus {number} is {sum}") 


def add_two(number):
    print(f"two plus {number} is {number+2}")


